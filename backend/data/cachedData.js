// Helper function to resolve state input to canonical name/code
export function resolveDmvUrl(stateInput) {
  if (!stateInput) return "https://www.usa.gov/motor-vehicle-services";
  const clean = stateInput.trim().toUpperCase().replace(/\s+/g, '_');
  return DMV_URLS[clean] || "https://www.usa.gov/motor-vehicle-services";
}

